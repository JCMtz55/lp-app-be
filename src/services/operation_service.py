from math import sqrt

def addition(num1: int, num2: int):
    print("Performing Addition")
    return num1 + num2

def substraction(num1: int, num2: int):
    return num1 - num2

def multiplication(num1: int, num2: int):
    return num1 * num2

def division(num1: int, num2: int):
    return num1 / num2

def square_root(num: int):
    return sqrt(num)


